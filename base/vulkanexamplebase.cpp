	ImGui::End();  // 结束窗口
	ImGui::PopStyleVar();  // 恢复样式变量
	ImGui::Render();  // 渲染 ImGui

	ui.update(currentBuffer);  // 更新 UI 叠加层（准备顶点缓冲区）

